"use client";
import React, { useEffect, useRef, useState } from 'react';
import { cn } from "@/lib/utils";

export const HalideLanding: React.FC = () => {
  const canvasRef = useRef<HTMLDivElement>(null);
  const layersRef = useRef<HTMLDivElement[]>([]);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;

    // Mouse Parallax Logic
    const handleMouseMove = (e: MouseEvent) => {
      const x = (window.innerWidth / 2 - e.pageX) / 25;
      const y = (window.innerHeight / 2 - e.pageY) / 25;

      // Rotate the 3D Canvas
      canvas.style.transform = `rotateX(${55 + y / 2}deg) rotateZ(${-25 + x / 2}deg)`;

      // Apply depth shift to layers
      layersRef.current.forEach((layer, index) => {
        if (!layer) return;
        const depth = (index + 1) * 15;
        const moveX = x * (index + 1) * 0.2;
        const moveY = y * (index + 1) * 0.2;
        layer.style.transform = `translateZ(${depth}px) translate(${moveX}px, ${moveY}px)`;
      });
    };

    // Entrance Animation
    canvas.style.opacity = '0';
    canvas.style.transform = 'rotateX(90deg) rotateZ(0deg) scale(0.8)';
    
    const timeout = setTimeout(() => {
      canvas.style.transition = 'all 2.5s cubic-bezier(0.16, 1, 0.3, 1)';
      canvas.style.opacity = '1';
      canvas.style.transform = 'rotateX(55deg) rotateZ(-25deg) scale(1)';
    }, 300);

    window.addEventListener('mousemove', handleMouseMove);

    return () => {
      window.removeEventListener('mousemove', handleMouseMove);
      clearTimeout(timeout);
    };
  }, []);

  return (
    <div className="relative h-screen w-full bg-[#0a0a0a] overflow-hidden flex items-center justify-center font-sans text-[#e0e0e0]">
      <style>{`
        .halide-grain {
          position: fixed;
          top: 0; left: 0; width: 100%; height: 100%;
          pointer-events: none;
          z-index: 100;
          opacity: 0.15;
        }

        .viewport {
          perspective: 2000px;
          width: 100vw; height: 100vh;
          display: flex; align-items: center; justify-content: center;
          overflow: hidden;
        }

        .canvas-3d {
          position: relative;
          width: 800px; height: 500px;
          transform-style: preserve-3d;
        }

        .layer {
          position: absolute;
          inset: 0;
          border: 1px solid rgba(224, 224, 224, 0.1);
          background-size: cover;
          background-position: center;
          transition: transform 0.5s ease;
        }

        .layer-1 { background-image: url('https://images.unsplash.com/photo-1464822759023-fed622ff2c3b?auto=format&fit=crop&q=80&w=1200'); filter: grayscale(1) contrast(1.2) brightness(0.5); }
        .layer-2 { background-image: url('https://images.unsplash.com/photo-1506744038136-46273834b3fb?auto=format&fit=crop&q=80&w=1200'); filter: grayscale(1) contrast(1.1) brightness(0.7); opacity: 0.6; mix-blend-mode: screen; }
        .layer-3 { background-image: url('https://images.unsplash.com/photo-1470770841072-f978cf4d019e?auto=format&fit=crop&q=80&w=1200'); filter: grayscale(1) contrast(1.3) brightness(0.8); opacity: 0.4; mix-blend-mode: overlay; }

        .contours {
          position: absolute;
          width: 200%; height: 200%;
          top: -50%; left: -50%;
          background-image: repeating-radial-gradient(circle at 50% 50%, transparent 0, transparent 40px, rgba(255,255,255,0.05) 41px, transparent 42px);
          transform: translateZ(120px);
          pointer-events: none;
        }
      `}</style>

      {/* SVG Filter for Grain */}
      <svg style={{ position: 'absolute', width: 0, height: 0 }}>
        <filter id="grain">
          <feTurbulence type="fractalNoise" baseFrequency="0.65" numOctaves="3" />
          <feColorMatrix type="saturate" values="0" />
        </filter>
      </svg>

      <div className="halide-grain" style={{ filter: 'url(#grain)' }}></div>

      <div className="absolute inset-0 p-16 grid grid-cols-2 grid-rows-[auto_1fr_auto] z-10 pointer-events-none">
        <div className="font-bold text-sm tracking-widest">HALIDE_CORE</div>
        <div className="text-right font-mono text-[#ff3c00] text-[0.7rem]">
          <div>LATITUDE: 34.0522Â° N</div>
          <div>FOCAL DEPTH: 80MM</div>
        </div>

        <h1 className="col-span-2 self-center text-[clamp(3rem,10vw,10rem)] leading-[0.85] tracking-tight mix-blend-difference font-black uppercase">
          SILVER<br />SULPHIDE
        </h1>

        <div className="col-span-2 flex justify-between items-end">
          <div className="font-mono text-[0.75rem] opacity-70">
            <p>[ ARCHIVE 2026 ]</p>
            <p>TOPOGRAPHICAL STUDY - AZ LIFE INSURANCE</p>
          </div>
          <a href="#" className="pointer-events-auto bg-[#e0e0e0] text-[#0a0a0a] px-8 py-4 font-bold transition-all hover:bg-[#ff3c00] hover:-translate-y-1 [clip-path:polygon(0_0,100%_0,100%_70%,85%_100%,0_100%)]">
            EXPLORE DEPTH
          </a>
        </div>
      </div>

      <div className="viewport">
        <div className="canvas-3d" ref={canvasRef}>
          <div className="layer layer-1" ref={(el) => { if (el) layersRef.current[0] = el; }}></div>
          <div className="layer layer-2" ref={(el) => { if (el) layersRef.current[1] = el; }}></div>
          <div className="layer layer-3" ref={(el) => { if (el) layersRef.current[2] = el; }}></div>
          <div className="contours"></div>
        </div>
      </div>

      <div className="absolute bottom-8 left-1/2 w-px h-16 bg-gradient-to-b from-[#e0e0e0] to-transparent animate-pulse origin-top"></div>
    </div>
  );
};
